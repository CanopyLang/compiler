{-# LANGUAGE OverloadedStrings #-}
{-# OPTIONS_GHC -Wall #-}

-- | Import error generation and validation for the Canopy build system.
--
-- This module provides functionality to generate comprehensive error reports
-- for import resolution failures. It creates detailed error information with
-- context about available modules and helpful suggestions for users.
--
-- === Primary Functionality
--
-- * Import error report generation ('toImportErrors')
-- * Known module collection from multiple sources ('gatherKnownModules')
-- * Unimported module identification ('calculateUnimportedModules')
-- * Import region mapping for error locations ('createRegionDict')
--
-- === Error Enhancement Process
--
-- Import error generation follows these steps:
--
-- 1. **Gather Context**: Collect all known modules from locals, foreigns, and results
-- 2. **Find Suggestions**: Identify available but unimported modules
-- 3. **Map Regions**: Create location mapping for import statements
-- 4. **Generate Errors**: Create comprehensive error reports with suggestions
--
-- === Usage Examples
--
-- @
-- -- Generate import error reports
-- let errors = toImportErrors env results imports problems
-- reportImportErrors errors
--
-- -- Gather available modules for suggestions
-- let knownModules = gatherKnownModules locals foreigns results
--     suggestions = Set.difference knownModules importedModules
-- @
--
-- === Error Enhancement Features
--
-- Enhanced error reports include:
--
-- * **Source Location**: Exact region where import appears in source
-- * **Module Context**: All available modules in project and dependencies
-- * **Suggestions**: Unimported modules that might resolve the issue
-- * **Problem Details**: Specific import problem type and description
--
-- === Module Source Categories
--
-- Known modules are gathered from:
--
-- * **Local Modules**: Project-specific modules being compiled
-- * **Foreign Modules**: External package dependencies
-- * **Result Modules**: Successfully compiled modules from current build
--
-- === Performance Characteristics
--
-- * **Time Complexity**: O(n + m + r) where n=locals, m=foreigns, r=results
-- * **Space Complexity**: O(k) where k=total known modules
-- * **Set Operations**: Efficient set-based operations for module suggestions
--
-- === Thread Safety
--
-- All functions are pure and thread-safe. The module uses immutable data
-- structures and functional transformation patterns.
--
-- @since 0.19.1
module Build.Validation.Imports
  ( -- * Import Error Generation
    toImportErrors
    -- * Module Context Analysis
  , gatherKnownModules
  , calculateUnimportedModules
    -- * Region and Location Mapping
  , createRegionDict
  , createImportError
  ) where

-- AST and module imports
import qualified AST.Source as Src
import qualified Canopy.Details as Details
import qualified Canopy.ModuleName as ModuleName

-- Build system imports
import Build.Types (Env (..), ResultDict)

-- Standard library imports
import Data.Map.Strict (Map)
import qualified Data.Map.Strict as Map
import qualified Data.NonEmptyList as NE
import Data.Set (Set)
import qualified Data.Set as Set
import qualified Reporting.Annotation as A
import qualified Reporting.Error.Import as Import

-- | Generate detailed import error reports.
--
-- Creates comprehensive error information for import resolution failures,
-- including context about available modules and helpful suggestions for
-- resolving import problems.
--
-- ==== Error Enhancement Process
--
-- 1. **Gather Known Modules**: Collect all available modules from locals,
--    foreigns, and compilation results
-- 2. **Calculate Suggestions**: Find modules that are available but not
--    currently imported
-- 3. **Create Region Map**: Map import statements to their source locations
-- 4. **Generate Enhanced Errors**: Transform problems into detailed error reports
--
-- ==== Context Enhancement
--
-- Each error report includes:
--
-- * **Source Region**: Exact location where import appears
-- * **Module Name**: The problematic import module name
-- * **Available Modules**: Set of all modules that could be imported
-- * **Problem Details**: Specific issue with the import
--
-- ==== Suggestion Algorithm
--
-- Suggestions are generated by:
--
-- * Finding all known modules in project and dependencies
-- * Excluding modules already imported in the current module
-- * Providing the difference as potential alternatives
--
-- @since 0.19.1
toImportErrors :: Env -> ResultDict -> [Src.Import] -> NE.List (ModuleName.Raw, Import.Problem) -> NE.List Import.Error
toImportErrors (Env _ _ _ _ _ locals foreigns) results imports problems =
  let knownModules = gatherKnownModules locals foreigns results
      unimportedModules = calculateUnimportedModules knownModules imports
      regionDict = createRegionDict imports
   in fmap (createImportError regionDict unimportedModules) problems

-- | Gather all known modules from various sources.
--
-- Collects module names from all available sources to provide comprehensive
-- context for import error reporting and suggestion generation.
--
-- ==== Module Sources
--
-- * **Foreign Modules**: External package dependencies (@Map.keysSet foreigns@)
-- * **Local Modules**: Project-specific modules (@Map.keysSet locals@)
-- * **Result Modules**: Successfully compiled modules (@Map.keysSet results@)
--
-- ==== Union Strategy
--
-- Uses @Set.unions@ to efficiently combine all module sources into a single
-- set. Automatically handles deduplication of modules that appear in
-- multiple sources.
--
-- ==== Performance Notes
--
-- * **Efficient Set Operations**: Uses Set.keysSet for O(1) key extraction
-- * **Single Union**: Combines all sources in one operation
-- * **Deduplication**: Automatic through Set data structure
--
-- @since 0.19.1
gatherKnownModules :: Map ModuleName.Raw Details.Local -> Map ModuleName.Raw Details.Foreign -> ResultDict -> Set ModuleName.Raw
gatherKnownModules locals foreigns results =
  Set.unions [Map.keysSet foreigns, Map.keysSet locals, Map.keysSet results]

-- | Calculate unimported modules for suggestions.
--
-- Identifies modules that are available in the project but not currently
-- imported, providing helpful suggestions for resolving import errors.
--
-- ==== Calculation Process
--
-- 1. **Extract Imported**: Get module names from all import statements
-- 2. **Find Available**: Use known modules from context gathering
-- 3. **Calculate Difference**: Subtract imported from available modules
--
-- ==== Suggestion Quality
--
-- The resulting set contains modules that:
--
-- * Are available for import in the current context
-- * Have not been imported in the current module
-- * Could potentially resolve import-related issues
--
-- @since 0.19.1
calculateUnimportedModules :: Set ModuleName.Raw -> [Src.Import] -> Set ModuleName.Raw
calculateUnimportedModules knownModules imports =
  Set.difference knownModules (Set.fromList (fmap Src.getImportName imports))

-- | Create region dictionary for import location mapping.
--
-- Maps import module names to their source code regions for precise
-- error location reporting in error messages.
--
-- ==== Region Extraction
--
-- For each import statement:
--
-- * **Extract Name**: Get module name from annotated import
-- * **Extract Region**: Get source region from annotation
-- * **Create Mapping**: Associate name with region in dictionary
--
-- ==== Import Structure Processing
--
-- Handles the nested structure @Src.Import (A.At region name) _ _@:
--
-- * @A.At region name@: Annotated module name with source location
-- * @_@: Import alias (not needed for region mapping)
-- * @_@: Import exposing (not needed for region mapping)
--
-- @since 0.19.1
createRegionDict :: [Src.Import] -> Map ModuleName.Raw A.Region
createRegionDict imports =
  Map.fromList (fmap (\(Src.Import (A.At region name) _ _) -> (name, region)) imports)

-- | Create import error from error components.
--
-- Combines region mapping, module suggestions, and problem details into
-- a comprehensive import error report for user feedback.
--
-- ==== Error Construction
--
-- Creates @Import.Error@ with:
--
-- * **Region**: Source location from region dictionary lookup
-- * **Module Name**: The problematic import module name
-- * **Unimported Modules**: Available modules for suggestions
-- * **Problem**: Specific import problem details
--
-- ==== Region Lookup
--
-- Uses @(!)@ operator for region lookup, which assumes that all problem
-- module names have corresponding entries in the region dictionary.
-- This invariant is maintained by the calling code in @toImportErrors@.
--
-- @since 0.19.1
createImportError :: Map ModuleName.Raw A.Region -> Set ModuleName.Raw -> (ModuleName.Raw, Import.Problem) -> Import.Error
createImportError regionDict unimportedModules (name, problem) =
  case Map.lookup name regionDict of
    Just region -> Import.Error region name unimportedModules problem
    Nothing -> Import.Error A.one name unimportedModules problem  -- Use default region if not found