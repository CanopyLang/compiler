{-# LANGUAGE OverloadedStrings #-}

-- | Unit.Generate.JavaScript.ESMGenTest - Tests for ESM code generation
--
-- This module provides unit tests for the pure functions in
-- "Generate.JavaScript.ESM". All tested functions are pure and can be
-- exercised in isolation without running the full compiler pipeline.
--
-- == Test Coverage
--
-- * moduleComment: exact Builder output for canonical module names
-- * canonicalToFilename: flat filename from canonical module name
-- * canonicalToPathBs: quoted path ByteString from canonical module name
-- * isExternal: cross-module vs same-module global classification
-- * isInternalNode: Cycle/Manager nodes excluded from exports
-- * buildExportItems: empty map yields empty list; non-empty yields ExportLocals
-- * buildImportItems: runtime import is always first
-- * renderModule: Dev and Prod modes produce correct output
-- * varToConst: Var statements converted to Const at module scope
-- * flattenCycleToModule: Block of Var statements flattened to ModuleStmt items
-- * groupDepsByModule: globals grouped by home module
-- * nodeDeps: dependency sets extracted correctly per node kind
-- * managerEffectDeps: correct dep sets for Cmd/Sub/Fx managers
--
-- @since 0.20.0
module Unit.Generate.JavaScript.ESMGenTest
  ( tests,
  )
where

