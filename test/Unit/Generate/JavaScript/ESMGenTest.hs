-- | Tests for 'ESM.moduleComment'.
moduleCommentTests :: TestTree
moduleCommentTests =
  testGroup
    "moduleComment"
    [ testCase "basics module comment exact expected string" $
        BB.toLazyByteString (ESM.moduleComment basicsHome)
          @?= "// elm.core.Basics.js\n// Auto-generated by the Canopy compiler. Do not edit.\n\n",
      testCase "list module comment exact expected string" $
        BB.toLazyByteString (ESM.moduleComment listHome)
          @?= "// elm.core.List.js\n// Auto-generated by the Canopy compiler. Do not edit.\n\n",
      testCase "module comment starts with //" $
        LBS.isPrefixOf "//" (BB.toLazyByteString (ESM.moduleComment basicsHome))
          @?= True,
      testCase "module comment ends with double newline" $
        LBS.isSuffixOf "\n\n" (BB.toLazyByteString (ESM.moduleComment basicsHome))
          @?= True
    ]

