-- | Tests for 'ESM.managerEffectDeps'.
--
-- Verifies the exact name sets for each 'Opt.EffectsType'.
managerEffectDepsTests :: TestTree
managerEffectDepsTests =
  testGroup
    "managerEffectDeps"
    [ testCase "Cmd manager has exactly init, onEffects, onSelfMsg, cmdMap" $
        ESM.managerEffectDeps Opt.Cmd
          @?= Set.fromList
            (map Name.fromChars ["init", "onEffects", "onSelfMsg", "cmdMap"]),
      testCase "Sub manager has exactly init, onEffects, onSelfMsg, subMap" $
        ESM.managerEffectDeps Opt.Sub
          @?= Set.fromList
            (map Name.fromChars ["init", "onEffects", "onSelfMsg", "subMap"]),
      testCase "Fx manager has exactly init, onEffects, onSelfMsg, cmdMap, subMap" $
        ESM.managerEffectDeps Opt.Fx
          @?= Set.fromList
            (map Name.fromChars ["init", "onEffects", "onSelfMsg", "cmdMap", "subMap"]),
      testCase "Cmd deps set has cardinality 4" $
        Set.size (ESM.managerEffectDeps Opt.Cmd) @?= 4,
      testCase "Sub deps set has cardinality 4" $
        Set.size (ESM.managerEffectDeps Opt.Sub) @?= 4,
      testCase "Fx deps set has cardinality 5" $
        Set.size (ESM.managerEffectDeps Opt.Fx) @?= 5,
      testCase "Cmd contains cmdMap but not subMap" $
        let deps = ESM.managerEffectDeps Opt.Cmd
        in ( Set.member (Name.fromChars "cmdMap") deps,
             Set.member (Name.fromChars "subMap") deps
           )
             @?= (True, False),
      testCase "Sub contains subMap but not cmdMap" $
        let deps = ESM.managerEffectDeps Opt.Sub
        in ( Set.member (Name.fromChars "subMap") deps,
             Set.member (Name.fromChars "cmdMap") deps
           )
             @?= (True, False),
      testCase "Fx contains both cmdMap and subMap" $
        let deps = ESM.managerEffectDeps Opt.Fx
        in ( Set.member (Name.fromChars "cmdMap") deps,
             Set.member (Name.fromChars "subMap") deps
           )
             @?= (True, True)
    ]
