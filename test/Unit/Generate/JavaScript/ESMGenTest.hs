-- | Tests for 'ESM.flattenCycleToModule'.
flattenCycleToModuleTests :: TestTree
flattenCycleToModuleTests =
  testGroup
    "flattenCycleToModule"
    [ testCase "Var at top level becomes one ModuleStmt Const" $
        let name = JsName.fromBuilder "c"
        in case ESM.flattenCycleToModule (JS.Var name (JS.Int 3)) of
             [JS.ModuleStmt (JS.Const _ _)] -> pure ()
             other -> assertFailure ("Expected [ModuleStmt (Const _)], got: " ++ show other),
      testCase "Block of two Var statements yields exactly two items" $
        let name1 = JsName.fromBuilder "a"
            name2 = JsName.fromBuilder "b"
            block = JS.Block [JS.Var name1 (JS.Int 1), JS.Var name2 (JS.Int 2)]
        in length (ESM.flattenCycleToModule block) @?= 2,
      testCase "Block of two Var statements yields all ModuleStmt Const items" $
        let name1 = JsName.fromBuilder "a"
            name2 = JsName.fromBuilder "b"
            block = JS.Block [JS.Var name1 (JS.Int 1), JS.Var name2 (JS.Int 2)]
        in case ESM.flattenCycleToModule block of
             [JS.ModuleStmt (JS.Const _ _), JS.ModuleStmt (JS.Const _ _)] -> pure ()
             other -> assertFailure ("Expected two ModuleStmt Const, got: " ++ show other),
      testCase "non-Block non-Var statement becomes single ModuleStmt" $
        case ESM.flattenCycleToModule (JS.Return (JS.Int 0)) of
          [JS.ModuleStmt _] -> pure ()
          other -> assertFailure ("Expected [ModuleStmt _], got: " ++ show other),
      testCase "empty Block produces empty list" $
        length (ESM.flattenCycleToModule (JS.Block [])) @?= 0
    ]

