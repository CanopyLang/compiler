-- | Tests for 'ESM.varToConst'.
varToConstTests :: TestTree
varToConstTests =
  testGroup
    "varToConst"
    [ testCase "Var becomes Const" $
        let name = JsName.fromBuilder "x"
        in case ESM.varToConst (JS.Var name (JS.Int 42)) of
             JS.Const _ _ -> pure ()
             other -> assertFailure ("Expected Const, got: " ++ show other),
      testCase "Block of Var becomes Block of Const" $
        let name = JsName.fromBuilder "y"
        in case ESM.varToConst (JS.Block [JS.Var name (JS.Bool True)]) of
             JS.Block [JS.Const _ _] -> pure ()
             other -> assertFailure ("Expected Block [Const _], got: " ++ show other),
      testCase "Return passes through as Return" $
        case ESM.varToConst (JS.Return (JS.Int 0)) of
          JS.Return _ -> pure ()
          other -> assertFailure ("Expected Return, got: " ++ show other),
      testCase "EmptyStmt passes through as EmptyStmt" $
        case ESM.varToConst JS.EmptyStmt of
          JS.EmptyStmt -> pure ()
          other -> assertFailure ("Expected EmptyStmt, got: " ++ show other),
      testCase "nested Block of Var converted at all levels" $
        let name = JsName.fromBuilder "z"
        in case ESM.varToConst (JS.Block [JS.Block [JS.Var name (JS.Int 1)]]) of
             JS.Block [JS.Block [JS.Const _ _]] -> pure ()
             other -> assertFailure ("Expected Block [Block [Const _]], got: " ++ show other)
    ]

