-- | Tests for 'ESM.buildImportItems'.
--
-- The first item must always be the bare runtime import.
buildImportItemsTests :: TestTree
buildImportItemsTests =
  testGroup
    "buildImportItems"
    [ testCase "first import is always the bare runtime import" $
        let items = ESM.buildImportItems Set.empty basicsHome Map.empty
        in case items of
             (JS.ImportBare path : _) ->
               path @?= "'./canopy-runtime.js'"
             _ -> assertFailure "Expected first item to be ImportBare './canopy-runtime.js'",
      testCase "no external deps yields exactly one item (runtime import)" $
        let items = ESM.buildImportItems Set.empty basicsHome Map.empty
        in length items @?= 1,
      testCase "external dep in reachable set adds a cross-module import" $
        let listGlobal = Opt.Global listHome (Name.fromChars "map")
            reachable = Set.singleton listGlobal
            entries =
              Map.singleton
                (Name.fromChars "add")
                ( Opt.Global basicsHome (Name.fromChars "add"),
                  Opt.Define (Opt.Int 0) (Set.singleton listGlobal)
                )
            items = ESM.buildImportItems reachable basicsHome entries
        in length items @?= 2,
      testCase "external dep yields ImportNamed as second item" $
        let listGlobal = Opt.Global listHome (Name.fromChars "map")
            reachable = Set.singleton listGlobal
            entries =
              Map.singleton
                (Name.fromChars "add")
                ( Opt.Global basicsHome (Name.fromChars "add"),
                  Opt.Define (Opt.Int 0) (Set.singleton listGlobal)
                )
            items = ESM.buildImportItems reachable basicsHome entries
        in case items of
             [_, JS.ImportNamed _ _] -> pure ()
             _ -> assertFailure "Expected [ImportBare, ImportNamed _]"
    ]

