-- | Tests for 'ESM.groupDepsByModule'.
groupDepsByModuleTests :: TestTree
groupDepsByModuleTests =
  testGroup
    "groupDepsByModule"
    [ testCase "empty set produces empty map" $
        ESM.groupDepsByModule Set.empty @?= Map.empty,
      testCase "single global forms one entry under its home" $
        let global = Opt.Global basicsHome (Name.fromChars "add")
        in Map.size (ESM.groupDepsByModule (Set.singleton global)) @?= 1,
      testCase "two globals from same home form one group of size two" $
        let g1 = Opt.Global basicsHome (Name.fromChars "add")
            g2 = Opt.Global basicsHome (Name.fromChars "sub")
            result = ESM.groupDepsByModule (Set.fromList [g1, g2])
        in fmap Set.size (Map.lookup basicsHome result) @?= Just 2,
      testCase "globals from two distinct homes produce two map entries" $
        let g1 = Opt.Global basicsHome (Name.fromChars "add")
            g2 = Opt.Global listHome (Name.fromChars "map")
        in Map.size (ESM.groupDepsByModule (Set.fromList [g1, g2])) @?= 2,
      testCase "three globals: two from basics, one from list yields correct groups" $
        let g1 = Opt.Global basicsHome (Name.fromChars "add")
            g2 = Opt.Global basicsHome (Name.fromChars "sub")
            g3 = Opt.Global listHome (Name.fromChars "map")
            result = ESM.groupDepsByModule (Set.fromList [g1, g2, g3])
        in do
             Map.size result @?= 2
             fmap Set.size (Map.lookup basicsHome result) @?= Just 2
             fmap Set.size (Map.lookup listHome result) @?= Just 1
    ]

