-- | Tests for 'ESM.buildExportItems'.
buildExportItemsTests :: TestTree
buildExportItemsTests =
  testGroup
    "buildExportItems"
    [ testCase "empty entries produce empty list" $
        length (ESM.buildExportItems Map.empty) @?= 0,
      testCase "entries with only Cycle nodes produce empty list" $
        let entries =
              Map.singleton
                (Name.fromChars "cycle")
                (Opt.Global basicsHome (Name.fromChars "cycle"), Opt.Cycle [] [] [] Set.empty)
        in length (ESM.buildExportItems entries) @?= 0,
      testCase "entries with only Manager nodes produce empty list" $
        let entries =
              Map.singleton
                (Name.fromChars "mgr")
                (Opt.Global basicsHome (Name.fromChars "mgr"), Opt.Manager Opt.Cmd)
        in length (ESM.buildExportItems entries) @?= 0,
      testCase "Enum entry produces exactly one item" $
        let global = Opt.Global basicsHome (Name.fromChars "myEnum")
            entries =
              Map.singleton
                (Name.fromChars "myEnum")
                (global, Opt.Enum Index.first)
        in length (ESM.buildExportItems entries) @?= 1,
      testCase "exportable entry produces ExportLocals item" $
        let global = Opt.Global basicsHome (Name.fromChars "myEnum")
            entries =
              Map.singleton
                (Name.fromChars "myEnum")
                (global, Opt.Enum Index.first)
        in case ESM.buildExportItems entries of
             [JS.ExportLocals _] -> pure ()
             other -> assertFailure ("Expected [ExportLocals _], got: " ++ show other),
      testCase "Box entry produces exactly one item" $
        let global = Opt.Global basicsHome (Name.fromChars "boxed")
            entries =
              Map.singleton
                (Name.fromChars "boxed")
                (global, Opt.Box)
        in length (ESM.buildExportItems entries) @?= 1
    ]

